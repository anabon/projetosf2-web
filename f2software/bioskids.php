A Fundação Bios Kids é um projeto realizado pelo time Bios e que tem como ideia principal solicitar para aos clientes, equipamentos sem uso ou danificados para que possam ser reformados e instalados para entidades e crianças carentes que necessitem para aprenderem usar sistemas de informática ou internet.<p>

Outro serviço disponibilizado são aulas de informática, oferecidas pelos voluntários para entidades que necessitarem. <p>

A Bios Computadores é a maior patrocinadora da Fundação Bios Kids e que junto com seus colaboradores demonstra a preocupação com a RESPONSABILIDADE SOCIAL em nossa cidade. Todas as empresas que colaboram com a Fundação, recebem um (Selo), como empresa participante do projeto.<p>

Todos os equipamentos adquiridos ou cedidos no projeto são exibidos e divulgados no site www.bios.com.br.

 
 
